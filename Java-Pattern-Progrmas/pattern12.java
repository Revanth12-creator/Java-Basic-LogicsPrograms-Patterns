class pattern12 {
public static void main(String args[])
 {
  for(int i=1;i<=5;i++)                        /*   1      */
  {                                            /*   12     */
   for(int j=1;j<=i;j++)                       /*   123    */                    
   {                                           /*   1234   */ 
    System.out.print(j);                       /*   12345  */
   }
  System.out.println();
  }
 }
}