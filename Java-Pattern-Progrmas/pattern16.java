class pattern16 
{
public static void main(String args[])
 {
  for(int i=1;i<=5;i++)                             /*   11111  */
  {                                                 /*   2222   */
   for(int j=5;j>=i;j--)                            /*   333    */                    
   {                                                /*   44     */ 
    System.out.print(i);                            /*   5      */
   }
  System.out.println();
  }
 }
}