class pattern17
{
public static void main(String args[])
 {
  for(int i=5;i>=1;i--)                             /*   12345  */
  {                                                 /*   1234  */
   for(int j=1;j<=i;j++)                            /*   123    */                    
   {                                                /*   12   */ 
    System.out.print(j);                            /*   1     */
   }
  System.out.println();
  }
 }
}