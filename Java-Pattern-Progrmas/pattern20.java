class pattern20
{
public static void main(String args[])
 {
  for(int i=5; i>=1; i--)                           /*   55555 */
  {                                                 /*   4444  */
   for(int j=1; j<=i; j++)                          /*   333   */                    
   {                                                /*   22    */ 
    System.out.print(i);                            /*   1     */
   }
  System.out.println();
  }
 }
}