class pattern21
{
public static void main(String args[])
 {
  for(int i=1; i<=5; i++)                           /*   54321  */
  {                                                 /*   5432   */
   for(int j=5; j>=i; j--)                          /*   543    */                    
   {                                                /*   54     */ 
    System.out.print(j);                            /*   5      */
   }
  System.out.println();
  }
 }
}