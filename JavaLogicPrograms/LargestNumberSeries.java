class LargestNumberSeries
{
 public static void main(String args[])
 {
   int a=10,b=40,c=30;
   if(a>=b && a>=c)
   {
    System.out.print(a+" is the Largest Number");
   }
   if(b>=a && b>=c)
   {
    System.out.print(b+" is the Largest Number");
   }
   if(c>=a && c>=b)
   {
    System.out.print(c+" is the Largest Number");
   }
 }
}


